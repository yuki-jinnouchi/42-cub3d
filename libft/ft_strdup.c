#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s1_cpy;

	i = 0;
	s1_cpy = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!s1_cpy)
		return (NULL);
	while (s1[i])
	{
		s1_cpy[i] = s1[i];
		i++;
	}
	s1_cpy[i] = '\0';
	return (s1_cpy);
}
